
piano = ['a','b','c','d','e','f','g']

print(piano[2:5])       # ['c', 'd', 'e']
print(piano[2:])        # ['c', 'd', 'e', 'f', 'g']
print(piano[:5])        # ['a', 'b', 'c', 'd', 'e']
print(piano[2:5:2])     # ['c', 'e']
print(piano[::2])       # ['a', 'c', 'e', 'g']
print(piano[::-1])      # ['g', 'f', 'e', 'd', 'c', 'b', 'a']
print(piano[::-1])      # ['g', 'f', 'e', 'd', 'c', 'b', 'a']



